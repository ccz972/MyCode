template <typename T> void chkmax(T &x, T y) { x = max(x, y); }
template <typename T> void chkmin(T &x, T y) { x = min(x, y); }

void solve() {
  ll l, r; cin >> l >> r;
  if (~l & 1) ++ l;
  if (~r & 1) -- r;
  // debug(l, r);
  if (l > r) {
    cout << "0\n";
  } else {
    cout << (r - l) / 2 + 1 << "\n";
  }
  
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  int T = 1; cin >> T;
  while(T -- ) solve();
  return 0;
}