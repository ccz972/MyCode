void solve() {
  string s; cin >> s; int n = s.size();
  int cnt = 0;
  for(int i = 0; i < n; i ++ ) {
  	if(s[i] == 'A') {
  		if(i < n - 1 && s[i + 1] == '4') {
  			cnt ++;
  		}
  	} else if(s[i] == '4') {
  		if(i < n - 2 && s[i + 1] == '4' && s[i + 2] == '0') {
  			cnt ++;
  			i += 2;
  		}
  	}
  	// cout << i << " " << cnt << "\n";
  }
  cout << cnt << "\n";
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);
  int T = 1; //cin >> T;
  while(T --) solve();

  return 0;
}
